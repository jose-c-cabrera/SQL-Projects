--NOW CREATE TABLES AND THEN  ADD VALUES TO THESE TABLES

create table PCLIENT (
	ClientID int CONSTRAINT PK_ClientID PRIMARY KEY,
	ClientLastName varchar(20) NOT NULL,
	ClientAddress varchar(30) NOT NULL,
	ClientTel varchar(20) NOT NULL, 
	ClientEmail varchar(20) NOT NULL,
	)

create table SALES_PROD (
	OrderID int NOT NULL,
	PurchaseDate date NOT NULL,
	ProductID int NOT NULL, 
	ClientID int NOT NULL, 
	Price money NOT NULL, 
	Quantity int NOT NULL,
	Total money NOT NULL, 
	PRIMARY KEY (OrderID, ProductID, ClientID)
	)

create table PRODUCTS ( 
	ProductID int NOT NULL,
	ProductName varchar(40) NOT NULL, 
	CategoryID int NOT NULL, 
	Price money NOT NULL, 
	Inventory int NOT NULL,
	Comment varchar(50) NULL,
	PRIMARY KEY(ProductID, CategoryID)
)
create table CATEGORY (
	CategoryID int CONSTRAINT PK_CategoryID PRIMARY KEY,
	CategoryName varchar(20) NOT NULL,
	Comment varchar(30) NULL
	)


